:<<!
给定一个file.txt：
987-123-4567
123 456 7890
(123) 456-7890

要求输出有效的电话号码：
987-123-4567
(123) 456-7890

分析：sed加入r模式，支持正则表达式。其正则表达式用两个//包含。
内部的正则表达式如下：^[0-9]{3}-[0-9]{3}-[0-9]{4}$|^\([0-9]{3}\) [0-9]{3}-[0-9]{4}$
是两个正则表达式的或：^[0-9]{3}-[0-9]{3}-[0-9]{4}$ 和 ^\([0-9]{3}\) [0-9]{3}-[0-9]{4}$
^表示开头，$表示结尾。

!

sed -rn '/^[0-9]{3}-[0-9]{3}-[0-9]{4}$|^\([0-9]{3}\) [0-9]{3}-[0-9]{4}$/p' file.txt
